
public class Passenger {
	public String seatingPref;
	public String classPref;

	
	Passenger(String sp, String cp){
		seatingPref = sp;
		classPref = cp;
	}
	
	public String getSeatingPref () {
		return seatingPref;
	}
	
	public String getClassPref () {
		return classPref;
	}
	


}
